!==============================================================================!
  subroutine Alias_Heat_Fluxes(Turb, ut, vt, wt)
!------------------------------------------------------------------------------!
!   Create aliases for turbulent heat fluxes.                                  !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Turb_Type), target  :: Turb
  type(Var_Type),   pointer :: ut, vt, wt
!==============================================================================!

  ut => Turb % ut
  vt => Turb % vt
  wt => Turb % wt

  end subroutine
