!==============================================================================!
  real function U_Plus_Rough_Walls(wall_dist)
!------------------------------------------------------------------------------!
!   Calculates U+ for rough walls.                                             !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Const_Mod, only: TINY
  use Rans_Mod,  only: z_o, kappa
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real :: wall_dist
!==============================================================================!

  U_Plus_Rough_Walls = log((wall_dist + z_o) / z_o) / (kappa + TINY) + TINY

  end function
