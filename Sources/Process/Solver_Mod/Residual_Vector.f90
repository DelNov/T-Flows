!==============================================================================!
  subroutine Residual_Vector(ni, r, b, a, x)
!------------------------------------------------------------------------------!
!   Calculates residual vector {r} = {b} - [A]{x}                              !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Comm_Mod
  use Matrix_Mod
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer           :: ni
  real              :: r(:)
  real              :: b(:)
  type(Matrix_Type) :: a
  real              :: x(:)
!-----------------------------------[Locals]-----------------------------------!
  integer  :: i, j, k, sub
!==============================================================================!

  !----------------!
  !   r = b - Ax   !
  !----------------!
  ! Why not callig this: call exchange(x) ???
  do i = 1, ni
    do j = a % row(i), a % row(i+1) - 1
      k = a % col(j)
      r(i) = b(i) - a % val(j) * x(k)
    end do
  end do

  end subroutine
