!==============================================================================!
  subroutine Alias_Solver(Sol, A, b)
!------------------------------------------------------------------------------!
!   Create alias for system matrix and right hand side.                        !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Solver_Type), target  :: Sol
  type(Matrix_Type),  pointer :: A
  real,               pointer :: b(:)
!==============================================================================!

  A => Sol % A
  b => Sol % b % val

  end subroutine
