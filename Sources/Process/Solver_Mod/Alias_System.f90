!==============================================================================!
  subroutine Solver_Mod_Alias_System(sol, a, b)
!------------------------------------------------------------------------------!
!   Create alias for system matrix and right hand side.                        !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Solver_Type), target  :: sol
  type(Matrix_Type), pointer :: a
  real,              pointer :: b(:)
!==============================================================================!

  a => sol % a
  b => sol % b % val

  end subroutine
