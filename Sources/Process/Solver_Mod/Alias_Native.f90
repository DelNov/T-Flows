!==============================================================================!
  subroutine Alias_Native(Sol, A, b)
!------------------------------------------------------------------------------!
!   Create alias for system matrix and right hand side.                        !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Solver_Type), target  :: Sol
  type(Matrix_Type),  pointer :: A
  real,               pointer :: b(:)
!==============================================================================!

  A => Sol % Nat % A
  b => Sol % Nat % b % val

  end subroutine
