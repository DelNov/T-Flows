!==============================================================================!
  subroutine Front_Mod_Clean(front)
!------------------------------------------------------------------------------!
!   Cleans the front                                                           !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Front_Type),  target :: front
!==============================================================================!

  !-----------------------------!
  !   Deallocate all elements   !
  !-----------------------------!
  deallocate(front % elem)
  front % n_elems = 0

  !-----------------------------!
  !   Deallocate all vertices   !
  !-----------------------------!
  deallocate(front % vert)
  front % n_verts = 0

  !-----------------------------!
  !   Deallocate all vertices   !
  !-----------------------------!
  deallocate(front % side)
  front % n_sides = 0

  end subroutine
