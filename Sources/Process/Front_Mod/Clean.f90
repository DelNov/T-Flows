!==============================================================================!
  subroutine Clean_Front(Front)
!------------------------------------------------------------------------------!
!   Cleans the front                                                           !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Front_Type),  target :: Front
!==============================================================================!

  !-----------------------------!
  !   Deallocate all elements   !
  !-----------------------------!
  deallocate(Front % Elem)
  Front % n_elems = 0

  !-----------------------------!
  !   Deallocate all vertices   !
  !-----------------------------!
  deallocate(Front % Vert)
  Front % n_verts = 0

  !-----------------------------!
  !   Deallocate all vertices   !
  !-----------------------------!
  deallocate(Front % side)
  Front % n_sides = 0

  end subroutine
