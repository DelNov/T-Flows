!==============================================================================!
  subroutine Field_Mod_Alias_Momentum(flow, u, v, w)
!------------------------------------------------------------------------------!
!   Creates aliases for velocity components.                                   !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Field_Type), target  :: flow
  type(Var_Type),   pointer :: u, v, w
!==============================================================================!

  u => flow % u
  v => flow % v
  w => flow % w

  end subroutine
