!==============================================================================!
  real function U_Plus_Rough_Walls(Turb, wall_dist)
!------------------------------------------------------------------------------!
!   Calculates U+ for rough walls.                                             !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Turb_Type) :: Turb
  real             :: wall_dist
!==============================================================================!

  U_Plus_Rough_Walls = log( (wall_dist + Turb % z_o) / Turb % z_o)  &
                            / (kappa + TINY) + TINY

  end function
