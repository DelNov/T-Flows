!==============================================================================!
  subroutine Alias_K_Eps(Turb, kin, eps)
!------------------------------------------------------------------------------!
!   Create aliases for k and epsilon.                                          !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Turb_Type), target  :: Turb
  type(Var_Type),   pointer :: kin, eps
!==============================================================================!

  kin => Turb % kin
  eps => Turb % eps

  end subroutine
