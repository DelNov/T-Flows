!==============================================================================!
  subroutine Turb_Mod_Alias_K_Eps(turb, kin, eps)
!------------------------------------------------------------------------------!
!   Create aliases for k and epsilon.                                          !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Turb_Type), target  :: turb
  type(Var_Type),  pointer :: kin, eps
!==============================================================================!

  kin => turb % kin
  eps => turb % eps

  end subroutine
