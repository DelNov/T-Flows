!==============================================================================!
  real function Y_Plus_Low_Re(Turb, u_tau, wall_dist, kin_vis)
!------------------------------------------------------------------------------!
!   Calculates y+ for low Reynolds approach.                                   !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Turb_Type) :: Turb
  real             :: u_tau, wall_dist, kin_vis
!==============================================================================!

  Y_Plus_Low_Re = max(u_tau * wall_dist / kin_vis, TINY)

  end function
