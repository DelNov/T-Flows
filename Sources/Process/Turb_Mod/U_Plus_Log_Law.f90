!==============================================================================!
  real function U_Plus_Log_Law(Turb, y_plus)
!------------------------------------------------------------------------------!
!   Calculates U+ from log law.                                                !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Turb_Type) :: Turb
  real              :: y_plus
!==============================================================================!

  U_Plus_Log_Law = log( max(y_plus, 1.05) * e_log ) / kappa

  end function
