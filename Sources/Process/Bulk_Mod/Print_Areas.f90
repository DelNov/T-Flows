!==============================================================================!
  subroutine Bulk_Mod_Print_Areas(bulk)
!------------------------------------------------------------------------------!
!   Compute mass fluxes through whole domain.                                  !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Bulk_Type) :: bulk
!==============================================================================!

  if(this_proc < 2) then
    write(*,'(a7,es12.5)') ' # Ax :', bulk % area_x
    write(*,'(a7,es12.5)') ' # Ay :', bulk % area_y
    write(*,'(a7,es12.5)') ' # Az :', bulk % area_z
  end if

  end subroutine
