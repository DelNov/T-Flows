!==============================================================================!
  subroutine Monitor_Mod_Write_4_Vars(n, var1, var2, var3, var4)
!------------------------------------------------------------------------------!
!   This is to set up monitoring points.                                       !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Var_Mod
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer        :: n
  type(Var_Type) :: var1, var2, var3, var4
!-----------------------------------[Locals]-----------------------------------!
  integer :: m
!==============================================================================!

  do m = 1, monitor % count  
    if(monitor % cell(m) > 0) then
      write(10+m,'(i9,4e16.6)')  n,  var1 % n(monitor % cell(m)),  &
                                     var2 % n(monitor % cell(m)),  &
                                     var3 % n(monitor % cell(m)),  &
                                     var4 % n(monitor % cell(m))
    end if
  end do

  end subroutine
