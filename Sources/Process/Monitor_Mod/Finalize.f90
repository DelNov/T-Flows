!==============================================================================!
  subroutine Monitor_Mod_Finalize(monitor)
!------------------------------------------------------------------------------!
!   Close the monitoring files.                                                !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Monitor_Type) :: monitor
!-----------------------------------[Locals]-----------------------------------!
  integer :: m
!==============================================================================!

  !--------------------------------!
  !   Close the monitoring files   !
  !--------------------------------!
  do m = 1, monitor % n_points
    if(monitor % cell(m) > 0) close(monitor % file_unit(m))
  end do

  end subroutine
