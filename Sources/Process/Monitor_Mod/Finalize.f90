!==============================================================================!
  subroutine Monitor_Mod_Finalize()
!------------------------------------------------------------------------------!
!   Close the monitoring files.                                                !
!------------------------------------------------------------------------------!
  implicit none
!-----------------------------------[Locals]-----------------------------------!
  integer :: m
!==============================================================================!

  !--------------------------------!
  !   Close the monitoring files   !
  !--------------------------------!
  do m = 1, monitor % count
    if(monitor % cell(m) > 0) close(10 + m)
  end do

  end subroutine
