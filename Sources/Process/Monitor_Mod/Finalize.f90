!==============================================================================!
  subroutine Finalize(Monitor)
!------------------------------------------------------------------------------!
!   Close the monitoring files.                                                !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Monitor_Type) :: Monitor
!-----------------------------------[Locals]-----------------------------------!
  integer :: m
!==============================================================================!

  !--------------------------------!
  !   Close the monitoring files   !
  !--------------------------------!
  do m = 1, Monitor % n_points
    if(Monitor % cell(m) > 0) close(Monitor % file_unit(m))
  end do

  end subroutine
