!==============================================================================!
  subroutine Alias_Native(Nat, A, b)
!------------------------------------------------------------------------------!
!   Create alias for system matrix and right hand side.                        !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Native_Type), target  :: Nat
  type(Matrix_Type),  pointer :: A
  real,               pointer :: b(:)
!==============================================================================!

  A => Nat % A
  b => Nat % b % val

  end subroutine
