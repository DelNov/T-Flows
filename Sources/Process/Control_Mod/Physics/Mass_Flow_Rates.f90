!==============================================================================!
  subroutine Control_Mod_Mass_Flow_Rates(bulk, verbose)
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Bulk_Mod, only: Bulk_Type
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Bulk_Type)   :: bulk
  logical, optional :: verbose
!-----------------------------------[Locals]-----------------------------------!
  real :: def(3)
  real :: val(3)
!==============================================================================!

  data def / 0.0, 0.0, 0.0 /

  call Control_Mod_Read_Real_Array('MASS_FLOW_RATES', 3, def,  &
                                    val, verbose)

  bulk % flux_x_o = val(1)
  bulk % flux_y_o = val(2)
  bulk % flux_z_o = val(3)

  end subroutine
