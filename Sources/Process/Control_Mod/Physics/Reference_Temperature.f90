!==============================================================================!
  subroutine Control_Mod_Reference_Temperature(field, verbose)
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Field_Mod, only: Field_Type
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Field_Type)  :: field
  logical, optional :: verbose
!-----------------------------------[Locals]-----------------------------------!
  real :: def
!==============================================================================!

  def = 20.0

  call Control_Mod_Read_Real_Item('REFERENCE_TEMPERATURE', def,  &
                                   field % t_ref, verbose)

  end subroutine
