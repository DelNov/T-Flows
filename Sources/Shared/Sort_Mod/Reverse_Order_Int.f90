!==============================================================================!
  subroutine Sort_Mod_Reverse_Order_Int(a)
!------------------------------------------------------------------------------!
!   Put an integer array in reverse order                                      !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer :: a(:)
!-----------------------------------[Locals]-----------------------------------!
  integer :: i, j, n
!==============================================================================!

  n = size(a, 1)

  ! Array too small, nothing to do
  if(n < 2) return

  ! Put in reverse order
  do i = 1, n / 2
    j = n - i + 1
    call Swap_Int(a(i), a(j))
  end do

  end subroutine
