!==============================================================================!
  subroutine Timer_Stop(Amg, i)
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[parameters]---------------------------------!
  class(Amg_Type) :: Amg
  integer         :: i
!==============================================================================!

  call cpu_time(Amg % tnew)

  Amg % time(i) = Amg % tnew - Amg % told

  end subroutine
