!==============================================================================!
  module Swap_Mod
!------------------------------------------------------------------------------!
!   This module is introduced just to check arguments to Swap routines         !
!------------------------------------------------------------------------------!
  implicit none
!==============================================================================!

  contains

!==============================================================================!
  subroutine Swap_Int(a, b)
!------------------------------------------------------------------------------!
!   Swaps two integers.                                                        !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer :: a, b
!-----------------------------------[Locals]-----------------------------------!
  integer :: t
!==============================================================================!

  t = a
  a = b
  b = t

  end subroutine

!==============================================================================!
  subroutine Swap_Real(a, b)
!------------------------------------------------------------------------------!
!   Swaps two real numbers.                                                    !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real :: a, b
!-----------------------------------[Locals]-----------------------------------!
  real :: t
!==============================================================================!

  t = a
  a = b
  b = t

  end subroutine

  end module
