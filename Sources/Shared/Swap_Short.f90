!==============================================================================!
  subroutine Swap_Short(a, b)
!------------------------------------------------------------------------------!
!   Swaps two short integers                                                   !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Const_Mod
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer(SP) :: a, b
!-----------------------------------[Locals]-----------------------------------!
  integer(SP) :: t
!==============================================================================!

  t = a
  a = b
  b = t

  end subroutine
