!==============================================================================!
  pure subroutine Invert_3x3_Matrix(Math, a, det)
!------------------------------------------------------------------------------!
!>  Inverts a 3x3 matrix.
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Math_Type), intent(in)    :: Math    !! parent class
  real,             intent(inout) :: a(3,3)  !! matrix to be inverted
  real,             intent(inout) :: det     !! matrix' determinant
!-----------------------------------[Locals]-----------------------------------!
  real    :: b(3,3)
  integer :: i
!------------------------[Avoid unused parent warning]-------------------------!
  Unused(Math)
!==============================================================================!

  det = a(1,1)*(a(2,2)*a(3,3)-a(2,3)*a(3,2))  &
      - a(1,2)*(a(2,1)*a(3,3)-a(2,3)*a(3,1))  &
      + a(1,3)*(a(2,1)*a(3,2)-a(2,2)*a(3,1))

  do i=1,3
    b(i,1) = a(i,1)
    b(i,2) = a(i,2)
    b(i,3) = a(i,3)
  enddo

  a(1,1) = b(2,2)*b(3,3) - b(2,3)*b(3,2)
  a(2,1) = b(2,3)*b(3,1) - b(2,1)*b(3,3)
  a(3,1) = b(2,1)*b(3,2) - b(2,2)*b(3,1)

  a(1,2) = b(1,3)*b(3,2) - b(1,2)*b(3,3)
  a(2,2) = b(1,1)*b(3,3) - b(1,3)*b(3,1)
  a(3,2) = b(1,2)*b(3,1) - b(1,1)*b(3,2)

  a(1,3) = b(1,2)*b(2,3) - b(1,3)*b(2,2)
  a(2,3) = b(1,3)*b(2,1) - b(1,1)*b(2,3)
  a(3,3) = b(1,1)*b(2,2) - b(1,2)*b(2,1)

  a(:,:) = a(:,:) / det

  end

