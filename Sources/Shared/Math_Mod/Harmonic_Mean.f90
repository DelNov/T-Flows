!==============================================================================!
  real function Math_Mod_Harmonic_Mean(a, b)
!------------------------------------------------------------------------------!
!   Finds harmonic mean of two input arguments, "a" and "b"                    !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real :: a, b
!==============================================================================!

  Math_Mod_Harmonic_Mean = 2.0 / (1.0 / a + 1.0 / b)

  end function
