!==============================================================================!
  subroutine Comm_Mod_Load_Maps(grid)
!------------------------------------------------------------------------------!
!   Fill the cell map for sequential version.                                  !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Grid_Mod
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Grid_Type) :: grid
!-----------------------------------[Locals]-----------------------------------!
  integer :: c, s
!==============================================================================!

  !----------------------------------------------------------------------!
  !   For sequential run, no need to read the map, just set dimensions   !
  !----------------------------------------------------------------------!
  nc_s  = grid % n_cells
  nb_s  = grid % n_bnd_cells
  nf_s  = grid % n_faces
  nbf_s = 0
  nc_t  = nc_s
  nb_t  = nb_s
  nf_t  = nf_s

  end subroutine
