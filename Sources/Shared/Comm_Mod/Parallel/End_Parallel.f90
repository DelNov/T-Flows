!==============================================================================!
  subroutine End_Parallel(Global)
!------------------------------------------------------------------------------!
!   Ends parallel execution of the program                                     !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Comm_Type) :: Global
!-----------------------------------[Locals]-----------------------------------!
  integer :: error
!------------------------[Avoid unused parent warning]-------------------------!
  Unused(Global)
!==============================================================================!

  call Mpi_Barrier(MPI_COMM_WORLD, error)
  call Mpi_Finalize(error)

  end subroutine
