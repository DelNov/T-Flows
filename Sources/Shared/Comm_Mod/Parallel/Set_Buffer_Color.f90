!==============================================================================!
  subroutine Comm_Mod_Set_Buffer_Color(grid)
!------------------------------------------------------------------------------!
!   Add one boundary condition type for buffer cells.                          !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use Grid_Mod
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Grid_Type) :: grid
!-----------------------------------[Locals]-----------------------------------!
  integer :: c, n
!==============================================================================!

  if(n_proc .eq. 0) return

  n = grid % n_bnd_cond
  grid % bnd_cond % type(n+1) = BUFFER
  do c = -1, -grid % n_bnd_cells, -1
    if(grid % bnd_cond % color(c) .eq. BUFFER) then
      grid % bnd_cond % color(c) = n+1
    end if
  end do

  end subroutine
