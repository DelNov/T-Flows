!==============================================================================!
  subroutine Comm_Mod_Close_File(fh)
!------------------------------------------------------------------------------!
!   Close file for parallel runs.                                              !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer :: fh  ! file handle
!-----------------------------------[Locals]-----------------------------------!
  integer :: error
!==============================================================================!

  ! Close the file
  call Mpi_File_Close(fh, error)

  end subroutine
