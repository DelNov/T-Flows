!==============================================================================!
  subroutine Wait(Global)
!------------------------------------------------------------------------------!
!   Puts barrier for parallel execution.                                       !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Comm_Type) :: Global
!-----------------------------------[Locals]-----------------------------------!
  integer :: error
!------------------------[Avoid unused parent warning]-------------------------!
  Unused(Global)
!==============================================================================!

  call Mpi_Barrier(MPI_COMM_WORLD, error)

  end subroutine
