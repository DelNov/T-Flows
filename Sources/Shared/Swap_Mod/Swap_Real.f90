!==============================================================================!
  pure subroutine Swap_Real(a, b)
!------------------------------------------------------------------------------!
!>  Swap_Real is a subroutine in Swap_Mod for swapping two ral values. It
!>  takes two real numbers as input and exchanges their values.
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real, intent(inout) :: a, b
!-----------------------------------[Locals]-----------------------------------!
  real :: c
!==============================================================================!

  c = a
  a = b
  b = c

  end subroutine

