!==============================================================================!
  pure subroutine Swap_Int(a, b)
!------------------------------------------------------------------------------!
!>  Swap_Int is a subroutine in Swap_Mod for swapping two integer values. It
!>  takes two integer numbers as input and exchanges their values.
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer, intent(inout) :: a, b
!-----------------------------------[Locals]-----------------------------------!
  integer :: c
!==============================================================================!

  c = a
  a = b
  b = c

  end subroutine

