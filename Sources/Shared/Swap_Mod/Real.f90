!==============================================================================!
  subroutine Swap_Real(a, b)
!------------------------------------------------------------------------------!
!   Swaps two real numbers.                                                    !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real :: a, b
!-----------------------------------[Locals]-----------------------------------!
  real :: t
!==============================================================================!

  t = a
  a = b
  b = t

  end subroutine

