!==============================================================================!
  real function Triangle_Area_Z(xa, ya,   &
                                xb, yb,   &
                                xc, yc)
!------------------------------------------------------------------------------!
!   Area of a tringle in xy plane defined with nodes "a", "b", "c"
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real, intent(in) :: xa, ya, xb, yb, xc, yc
!==============================================================================!
!                                                                              !
!                c                                                             !
!               / \                                                            !
!              /   \                                                           !
!             /     \                                                          !
!            /       \                                                         !
!           /         \                                                        !
!          a-----------b                                                       !
!                                                                              !
!------------------------------------------------------------------------------!

  Triangle_Area_Z = abs(xa * (yb-yc) + xb * (yc-ya) + xc * (ya-yb)) * 0.5

  end function
